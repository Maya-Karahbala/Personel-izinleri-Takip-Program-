        boolean sonuc = true;
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PersonelIzinTakipiAppDatatbasePU");
        // update personel bilgileri
        EntityManager em3 = emf.createEntityManager();
        Query q3 = em3.createQuery("SELECT P FROM Personel P");
        personeller = q3.getResultList();
        //
        Personel secilen_personel = personeller.get(cmbPersonelS.getSelectedIndex());
        if (cmbPersonelS.getSelectedIndex() == -1) {
            lblperS.setVisible(true);
            lblperS.setText("personel secilmedi");
            sonuc = false;
        }
        ////////
        if (jDateChooserS.getDate() == null || jDateChooserS.getDate().before(date)) {
            lblTarihS.setVisible(true);
            lblTarihS.setText("Geçerli Bir tarih Giriniz");
            sonuc = false;
        }
        ///

        String desenSaat = "\\b([01][0-9]|2[0-3]):[0-5][0-9]:[0-5][0-9]\\b";
        String saat1 = txtbasSaati.getText();
        Pattern p = Pattern.compile(desenSaat);
        Matcher m = p.matcher(saat1);
        boolean sonuc1 = false;
        while (m.find()) {
            sonuc1 = true;
        }
        if (!sonuc1) {
            lblbasSaat.setVisible(true);
            lblbasSaat.setText("Geçerli Bir saat Giriniz");
        }
        //bitis saati
        String saat2 = txtBitisSaat.getText();

        Matcher m2 = p.matcher(saat2);
        boolean sonuc2 = false;
        while (m2.find()) {

            sonuc2 = true;
        }
        if (!sonuc2) {
            lblbasSaat.setVisible(true);

            lblbitSaat1.setText("Geçerli Bir saat Giriniz");
        }
        //yillik izin kontrulu
        Integer sure = 0;
        String basSaat = txtbasSaati.getText();
        String bitisSaat = txtBitisSaat.getText();
        try {

            SimpleDateFormat format = new SimpleDateFormat("HH:mm:ss");

            Date time1 = format.parse(basSaat);
            Date time2 = format.parse(bitisSaat);
            long dif = time2.getTime() - time1.getTime();
            sure = Integer.valueOf(String.valueOf(TimeUnit.MILLISECONDS.toHours(dif)));
        } catch (ParseException ex) {
            //Logger.getLogger(frm_yeniIzinEkle.class.getName()).log(Level.SEVERE, null, ex);
               lblbitSaat1.setText("Geçerli Bir saat Giriniz");
        }
//        //
//        (person.kalan_izin_MiktarHesapla() == 0
//                            && (person.kulllanilanSaat + Integer.parseInt(lblSure1.getText())) <= 7
        boolean sonuc3 = false;
        if (secilen_personel.getKalanIzin() > 1
                || String.valueOf(cmbTurS.getSelectedItem()).equals("ucretsiz")
                || (secilen_personel.getKalanIzin() == 1 && (secilen_personel.getKullanilanSaat() + sure) <= 7)) {
            sonuc3 = true;
            // String.valueOf(cmbTurS.getSelectedItem().equals("ucretsiz")   
        }
       // SaatlikIzinTekrarlamaKontrolu(secilen_personel);
        if (sonuc == sonuc1 == sonuc2 == sonuc3 == SaatlikIzinTekrarlamaKontrolu(secilen_personel) == true) {
            lblbasSaat.setVisible(false);
            lblbasSaat.setVisible(false);
            lblTarihS.setVisible(false);
            lblperS.setVisible(false);
            //sure hesaplamak

            if (sure < 1) {
                lblbitSaat1.setVisible(true);
                lblbitSaat1.setText("girdiginiz bilgilere dikkat ediniz");
            } else if (sure >= 7) {
                lblbitSaat1.setVisible(true);
                lblbitSaat1.setText("gunluk izin verin");
            } else {
                lblbitSaat1.setVisible(false);
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                //Integer id, Integer pid, String tur, Date tarih, String basSaat, String bitSaat, Integer sure
                em.persist(new SaatlikIzin1(EnbuyukIdS, secilen_personel.getId(), cmbTurS.getSelectedItem().toString(), jDateChooserS.getDate(), basSaat, bitisSaat, sure));
                em.getTransaction().commit();
                // personel bilgileri degistirmek
                if (!String.valueOf(cmbTurS.getSelectedItem()).equals("ucretsiz")) {
                    EntityManager em2 = emf.createEntityManager();
                    Query q = em2.createQuery("UPDATE Personel P SET P.kullanilanSaat=:KullanilanSaat, p.kalanSaat=:KalanSaat,p.kullanilanIzin=:KullanilanIzin,p.kalanIzin=:KalanIzin where p.id=:Id");
                    Integer KullanilanSaat = 0, kalanSaat = 0, KalanIzin = 0, KullanilanIzin = 0;
                    Integer toplamSure = sure + secilen_personel.getKullanilanSaat();
                    if (toplamSure >= 7) {

                        if ((toplamSure - 7) == 0) {
                            kalanSaat = 0;
                        } else {
                            kalanSaat = 7 - (toplamSure - 7);
                        }
                        KullanilanSaat = toplamSure - 7;

                        KullanilanIzin = secilen_personel.getKullanilanIzin() + 1;
                        KalanIzin = secilen_personel.getKalanIzin() - 1;

                    } else if (toplamSure < 7) {

                        KullanilanSaat = toplamSure;
                        kalanSaat = 7 - toplamSure;
                        KullanilanIzin = secilen_personel.getKullanilanIzin();
                        KalanIzin = secilen_personel.getKalanIzin();
                    }
                    q.setParameter("KullanilanSaat", KullanilanSaat);
                    q.setParameter("KalanSaat", kalanSaat);
                    q.setParameter("KullanilanIzin", KullanilanIzin);
                    q.setParameter("KalanIzin", KalanIzin);
                    q.setParameter("Id", secilen_personel.getId());

                    em2.getTransaction().begin();
                    q.executeUpdate();
                    em2.getTransaction().commit();
                }
                clear_SaatlikIzin();

            }

        } else if (!sonuc3) {
            JOptionPane.showMessageDialog(this, "yillik izin haki bittmis, ucretsiz izin verilebilir");
        } else if (!SaatlikIzinTekrarlamaKontrolu(secilen_personel)) {
            JOptionPane.showMessageDialog(this, "Izin daha once kaydedildi");
        }
          

