    public boolean gunlukIzinTekrarlamaKontrolu(Personel secilen_personel) {
        boolean sonuc = true;
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PersonelIzinTakipiAppDatatbasePU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT g FROM GunlukIzin g");
        izinlerG = q.getResultList();
        for (GunlukIzin gunlukIzin : izinlerG) {
            if (gunlukIzin.getPid() == secilen_personel.getId()) {
                if (jDateChooserbasG.getDate().after(gunlukIzin.getBaslangicTarihi())
                   && jDateChooserbitisG.getDate().before(gunlukIzin.getBitisTarihi())) {
                    sonuc = false;
                    break;
                } else if (jDateChooserbasG.getDate().before(gunlukIzin.getBaslangicTarihi())
                        && jDateChooserbitisG.getDate().after(gunlukIzin.getBaslangicTarihi())) {
                    sonuc = false;
                    break;
                } else if (jDateChooserbasG.getDate().before(gunlukIzin.getBitisTarihi())
                        && jDateChooserbitisG.getDate().after(gunlukIzin.getBitisTarihi())) {
                    sonuc = false;
                    break;
                } else if (jDateChooserbasG.getDate().before(gunlukIzin.getBaslangicTarihi())
                        && jDateChooserbitisG.getDate().after(gunlukIzin.getBitisTarihi())) {
                    sonuc = false;
                    break;
                } else if (jDateChooserbasG.getDate().equals(gunlukIzin.getBaslangicTarihi())
                        || jDateChooserbasG.getDate().equals(gunlukIzin.getBitisTarihi())
                       || jDateChooserbitisG.getDate().equals(gunlukIzin.getBaslangicTarihi())
                       || jDateChooserbitisG.getDate().equals(gunlukIzin.getBitisTarihi())) {
                    sonuc = false;//*
                    break;
                }
            }

        }
        ////////////
        EntityManager em2 = emf.createEntityManager();
        Query q2 = em2.createQuery("SELECT s FROM SaatlikIzin1 s");
        izinlerS = q2.getResultList();
        for (SaatlikIzin1 saatlikIzin : izinlerS) {
            if (saatlikIzin.getPid() == secilen_personel.getId()) {
                if (saatlikIzin.getTarih().equals(jDateChooserbasG.getDate())
                   || saatlikIzin.getTarih().equals(jDateChooserbitisG.getDate())) {
                    sonuc = false;
                    break;
                } else if (jDateChooserbasG.getDate().before(saatlikIzin.getTarih())
                        && jDateChooserbitisG.getDate().after(saatlikIzin.getTarih())) {
                    sonuc = false;
                    break;
                }
            }
        }
        return sonuc;

    }

